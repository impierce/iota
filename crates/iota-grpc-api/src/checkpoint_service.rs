use std::{pin::Pin, sync::Arc};

use iota_grpc_types::v0::checkpoints as grpc_checkpoints;
use tokio_util::sync::CancellationToken;
use tonic::{Request, Response, Status};
use tracing::{debug, info};

use crate::types::*;

pub struct CheckpointGrpcService {
    pub reader: Arc<GrpcReader>,
    pub checkpoint_summary_broadcaster: GrpcCheckpointSummaryBroadcaster,
    pub checkpoint_data_broadcaster: GrpcCheckpointDataBroadcaster,
    pub cancellation_token: CancellationToken,
}

impl CheckpointGrpcService {
    /// Create a new CheckpointGrpcService with a GrpcReader
    pub fn new(
        reader: Arc<GrpcReader>,
        checkpoint_summary_broadcaster: GrpcCheckpointSummaryBroadcaster,
        checkpoint_data_broadcaster: GrpcCheckpointDataBroadcaster,
        cancellation_token: CancellationToken,
    ) -> Self {
        Self {
            reader,
            checkpoint_summary_broadcaster,
            checkpoint_data_broadcaster,
            cancellation_token,
        }
    }
}

impl CheckpointGrpcService {
    fn stream_checkpoint_data(
        &self,
        start_sequence_number: Option<u64>,
        end_sequence_number: Option<u64>,
    ) -> impl futures::Stream<Item = CheckpointStreamResult> + Send {
        let rx = self.checkpoint_data_broadcaster.subscribe();
        self.reader.create_checkpoint_data_stream(
            rx,
            start_sequence_number,
            end_sequence_number,
            self.cancellation_token.clone(),
        )
    }

    fn stream_checkpoint_summary(
        &self,
        start_sequence_number: Option<u64>,
        end_sequence_number: Option<u64>,
    ) -> impl futures::Stream<Item = CheckpointStreamResult> + Send {
        let rx = self.checkpoint_summary_broadcaster.subscribe();
        self.reader.create_checkpoint_summary_stream(
            rx,
            start_sequence_number,
            end_sequence_number,
            self.cancellation_token.clone(),
        )
    }
}

// The `CheckpointService` is the auto-generated trait from the protobuf
// definition. It's generated by tonic/protobuf and defines the interface that
// any gRPC checkpoint service must implement.
#[tonic::async_trait]
impl grpc_checkpoints::checkpoint_service_server::CheckpointService for CheckpointGrpcService {
    type StreamCheckpointsStream =
        Pin<Box<dyn futures::Stream<Item = Result<grpc_checkpoints::Checkpoint, Status>> + Send>>;

    async fn stream_checkpoints(
        &self,
        request: Request<grpc_checkpoints::CheckpointStreamRequest>,
    ) -> Result<Response<Self::StreamCheckpointsStream>, Status> {
        let req = request.into_inner();
        let start_sequence_number = req.start_sequence_number;
        let end_sequence_number = req.end_sequence_number;
        let full = req.full;

        let stream: Self::StreamCheckpointsStream = if full {
            Box::pin(self.stream_checkpoint_data(start_sequence_number, end_sequence_number))
        } else {
            Box::pin(self.stream_checkpoint_summary(start_sequence_number, end_sequence_number))
        };
        Ok(Response::new(stream))
    }

    async fn get_epoch_first_checkpoint_sequence_number(
        &self,
        request: Request<grpc_checkpoints::EpochRequest>,
    ) -> Result<Response<grpc_checkpoints::CheckpointSequenceNumberResponse>, Status> {
        let epoch = request.into_inner().epoch;
        debug!(
            "get_epoch_first_checkpoint_sequence_number called for epoch {}",
            epoch
        );

        let sequence_number = if epoch == 0 {
            // Genesis epoch starts at checkpoint 0
            0
        } else {
            // Get the last checkpoint of the previous epoch
            match self.reader.get_epoch_last_checkpoint(epoch - 1) {
                Ok(Some(last_checkpoint)) => {
                    // First checkpoint of current epoch is the next one
                    *last_checkpoint.sequence_number() + 1
                }
                Ok(None) => {
                    return Err(Status::not_found(format!(
                        "No checkpoints found for previous epoch {}",
                        epoch - 1
                    )));
                }
                Err(e) => {
                    return Err(Status::internal(format!(
                        "Failed to get last checkpoint for epoch {}: {}",
                        epoch - 1,
                        e
                    )));
                }
            }
        };

        info!(
            "First checkpoint for epoch {}: seq={}",
            epoch, sequence_number
        );

        Ok(Response::new(
            grpc_checkpoints::CheckpointSequenceNumberResponse { sequence_number },
        ))
    }
}
